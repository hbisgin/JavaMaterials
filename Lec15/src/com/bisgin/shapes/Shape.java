package com.bisgin.shapes;

public class Shape {

	double area;
	double perimeter;
	
	public void setArea(double area)
	{
		this.area = area;
	}
	
	public double getArea()
	{
		return area;
	}
	
	public void setPerimeter(double perimeter)
	{
		this.perimeter = perimeter;
	}
	
	public double getPerimeter()
	{
		return perimeter;
	}
	
}
